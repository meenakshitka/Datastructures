public class ListNode {

    private int data;
    private ListNode next;

    public int getData() {
        return data;
    }
    public void setData(int data) {
        this.data = data;
    }

    public ListNode getNext() {
        return next;
    }

    public void setNext(ListNode link) {
        this.next = link;
    }
}
